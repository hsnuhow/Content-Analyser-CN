    def _calculate_visual_weight(self, node, soup: BeautifulSoup) -> float:
        """計算元素的視覺權重（位置、大小估算）"""
        try:
            # 安全檢查
            if node is None or not hasattr(node, 'get'):
                return 1.0

            # 檢查是否在頁面中央區域
            classes = node.get('class')
            if classes:
                classes_str = ' '.join(str(c) for c in classes).lower()
            else:
                classes_str = ''

            id_attr = node.get('id')
            id_str = str(id_attr).lower() if id_attr else ''

            weight = 1.0

            # 中央容器加分
            if any(x in classes_str or x in id_str for x in ['main', 'content', 'center', 'article']):
                weight += 0.3

            # 側邊欄減分
            if any(x in classes_str or x in id_str for x in ['side', 'sidebar', 'widget', 'aside']):
                weight -= 0.5

            # 頁首頁尾減分
            if any(x in classes_str or x in id_str for x in ['header', 'footer', 'nav']):
                weight -= 0.4

            return max(0.1, weight)
        except Exception:
            return 1.0

    def _calculate_dom_depth(self, node) -> int:
        """計算 DOM 深度"""
        try:
            if not node:
                return 0

            depth = 0
            current = node
            while current and hasattr(current, 'parent'):
                depth += 1
                current = current.parent
                if depth > 50:  # 防止無限循環
                    break
            return depth
        except Exception:
            return 5  # 返回一個合理的預設值

    def _calculate_paragraph_quality(self, node) -> float:
        """評估段落質量"""
        try:
            if not node or not hasattr(node, 'find_all'):
                return 0.0

            paragraphs = node.find_all('p')
            if not paragraphs:
                return 0.0

            total_score = 0.0
            for p in paragraphs:
                try:
                    text = p.get_text(strip=True)
                    if len(text) < 20:  # 太短的段落
                        continue

                    # 計算標點符號密度
                    punctuation_count = sum(1 for c in text if c in '。，！？；：、')
                    punct_density = punctuation_count / max(len(text), 1)

                    # 合理的標點密度（0.03-0.15）得分較高
                    if 0.03 <= punct_density <= 0.15:
                        total_score += 1.0
                    else:
                        total_score += 0.5
                except Exception:
                    continue

            return total_score / max(len(paragraphs), 1)
        except Exception:
            return 0.0

    def _calculate_chinese_ratio(self, text: str) -> float:
        """計算中文字符比例"""
        if not text:
            return 0.0
        chinese_chars = sum(1 for c in text if '\u4e00' <= c <= '\u9fff')
        return chinese_chars / max(len(text), 1)

    def _advanced_score_node(self, node, soup: BeautifulSoup) -> Tuple[float, Dict[str, float]]:
        """
        增強型節點評分系統
        返回: (總分, 各維度得分字典)
        """
        scores = {}

        try:
            # 安全檢查節點有效性
            if not node or not hasattr(node, 'get_text'):
                return 0.0, {}

            # 1. 文本長度 (權重 30%)
            try:
                text = node.get_text("\n", strip=True)
                text_len = len(text)
                scores['text_length'] = text_len * 0.3
            except Exception:
                text = ""
                text_len = 0
                scores['text_length'] = 0.0

            # 2. 段落質量 (權重 25%)
            try:
                para_quality = self._calculate_paragraph_quality(node)
                scores['paragraph_quality'] = para_quality * 1000 * 0.25
            except Exception:
                scores['paragraph_quality'] = 0.0

            # 3. 連結密度 (權重 15%)，越低越好
            try:
                links = node.find_all('a')
                link_text = ''.join(a.get_text(strip=True) for a in links)
                link_density = len(link_text) / max(text_len, 1)
                scores['link_density'] = (1 - link_density) * 500 * 0.15
            except Exception:
                scores['link_density'] = 0.0

            # 4. DOM 深度 (權重 10%)，適中深度較好
            try:
                depth = self._calculate_dom_depth(node)
                optimal_depth = 8
                depth_score = 1.0 - abs(depth - optimal_depth) / max(depth, optimal_depth)
                scores['dom_depth'] = depth_score * 300 * 0.10
            except Exception:
                scores['dom_depth'] = 0.0

            # 5. 視覺權重 (權重 10%)
            try:
                visual_weight = self._calculate_visual_weight(node, soup)
                scores['visual_weight'] = visual_weight * 400 * 0.10
            except Exception:
                scores['visual_weight'] = 0.0

            # 6. 中文密度 (權重 10%)
            try:
                chinese_ratio = self._calculate_chinese_ratio(text)
                scores['chinese_ratio'] = chinese_ratio * 300 * 0.10
            except Exception:
                scores['chinese_ratio'] = 0.0

            # 總分
            total_score = sum(scores.values())

            return total_score, scores

        except Exception as e:
            print(f"[評分異常] {e}")
            return 0.0, {}

    def _build_candidates(self, soup: BeautifulSoup, url: str = "") -> List[Any]:
        """構建候選節點列表，包含模板匹配"""
        candidates = []
        seen = set()

        def _push(nodes):
            for n in nodes:
                try:
                    # 安全檢查：確保節點有效且未被處理
                    if n and id(n) not in seen and hasattr(n, 'get_text'):
                        candidates.append(n)
                        seen.add(id(n))
                except Exception:
                    continue

        # 1. 先嘗試網站模板
        template = self._detect_site_template(soup, url)
        if template:
            print(f"[模板識別] 檢測到 {template} 網站模式")
            for sel in SITE_TEMPLATES[template]['selectors']:
                try:
                    _push(soup.select(sel))
                except Exception:
                    continue

        # 2. 通用選擇器
        for sel in MAIN_CONTENT_SELECTORS:
            try:
                _push(soup.select(sel))
            except Exception:
                continue

        # 3. 啟發式搜尋：包含多個段落或足夠長度的容器
        for node in soup.find_all(['article', 'section', 'div', 'main']):
            try:
                # 安全檢查節點有效性
                if not node or not hasattr(node, 'find_all'):
                    continue

                p_children = node.find_all('p', recursive=True)
                text_len = len(node.get_text(strip=True))
                if len(p_children) >= 3 or text_len > 300:
                    _push([node])
            except Exception:
                continue

        return candidates

    def _best_by_heuristics(self, soup: BeautifulSoup, url: str = "") -> Tuple[Optional[Any], float, List[Any]]:
        """
        使用增強型啟發式算法選擇最佳節點
        返回: (最佳節點, 置信度, 前10候選)
        """
        try:
            candidates = self._build_candidates(soup, url)
            if not candidates:
                return None, 0.0, []

            scored = []
            for n in candidates:
                try:
                    # 確保節點有效
                    if not n or not hasattr(n, 'get_text'):
                        continue

                    score, details = self._advanced_score_node(n, soup)
                    if score > 0:  # 只保留有效評分的節點
                        scored.append((n, score, details))
                except Exception as e:
                    print(f"[評分錯誤] {e}")
                    continue

            if not scored:
                return None, 0.0, []

            scored.sort(key=lambda x: x[1], reverse=True)
            best, best_score, best_details = scored[0]
            second_score = scored[1][1] if len(scored) > 1 else 0.0

            # 計算置信度
            conf = self._calculate_confidence(best_score, second_score, best, soup)

            print(f"[評分詳情] 最佳候選得分: {best_score:.1f}, 置信度: {conf:.2f}")

            return best, conf, [n for n, _, _ in scored[:10]]

        except Exception as e:
            print(f"[啟發式分析錯誤] {e}")
            return None, 0.0, []

    def _calculate_confidence(self, best_score: float, second_score: float,
                            best_node: Any, soup: BeautifulSoup) -> float:
        """
        綜合計算置信度
        考慮因素：1) 與次佳的差距 2) 絕對分數 3) 結構特徵匹配度
        """
        # 1. 差距置信度 (40%)
        if best_score > 0:
            margin = max(best_score - second_score, 0.0) / best_score
            margin_conf = min(1.0, margin * 2)  # 差距越大越好
        else:
            margin_conf = 0.0

        # 2. 絕對分數置信度 (30%)
        # 假設理想分數在 800-2000 之間
        if best_score >= 1500:
            score_conf = 1.0
        elif best_score >= 800:
            score_conf = 0.7 + (best_score - 800) / 700 * 0.3
        else:
            score_conf = best_score / 800 * 0.7

        # 3. 結構特徵匹配度 (30%)
        structure_conf = 0.5  # 基礎值
        try:
            # 檢查是否有標題標籤
            if best_node.find(['h1', 'h2', 'h3']):
                structure_conf += 0.2
            # 檢查是否有時間標記
            if best_node.find(['time', '[datetime]']):
                structure_conf += 0.15
            # 檢查段落數量
            p_count = len(best_node.find_all('p'))
            if p_count >= 5:
                structure_conf += 0.15
        except Exception:
            pass

        structure_conf = min(1.0, structure_conf)

        # 加權平均
        final_conf = (margin_conf * 0.4 + score_conf * 0.3 + structure_conf * 0.3)

        return final_conf

    # ========= LLM 輔助：DOM 摘要 → Gemini 選 selector =========
    def _css_path(self, el) -> str:
        """生成 CSS 路徑"""
        try:
            parts = []
            cur = el
            while cur and getattr(cur, 'name', None) and cur.name != 'html':
                try:
                    ident = cur.name

                    # 安全獲取 id
                    el_id = cur.get('id') if hasattr(cur, 'get') else None
                    if el_id:
                        ident += f"#{el_id}"

                    # 安全獲取 classes
                    classes = cur.get('class') if hasattr(cur, 'get') else None
                    if classes and isinstance(classes, (list, tuple)):
                        ident += '.' + '.'.join(str(c) for c in classes[:3])  # 限 3 個避免過長

                    parts.append(ident)
                    cur = cur.parent if hasattr(cur, 'parent') else None
                except Exception:
                    break

                if len(parts) > 20:  # 防止過長
                    break

            return ' > '.join(reversed(parts))
        except Exception:
            return "unknown"

    def _build_dom_summary(self, soup: BeautifulSoup, max_count: int = 180) -> List[Dict[str, Any]]:
        """構建 DOM 摘要供 LLM 使用"""
        cands = []
        for node in soup.find_all(['article', 'section', 'div', 'main']):
            try:
                # 安全檢查節點有效性
                if not node or not hasattr(node, 'get_text') or not hasattr(node, 'find_all'):
                    continue

                text = node.get_text(' ', strip=True)
                pcount = len(node.find_all('p'))

                if pcount >= 3 or len(text) > 300:
                    links_text = ''.join(a.get_text(strip=True) for a in node.find_all('a'))
                    ld = len(links_text) / max(len(text), 1)

                    # 安全獲取 id 和 class
                    node_id = node.get('id') if hasattr(node, 'get') else None
                    node_class = node.get('class') if hasattr(node, 'get') else None

                    cands.append({
                        'css_path': self._css_path(node)[:512],
                        'id': node_id,
                        'class': node_class,
                        'text_len': len(text),
                        'p_count': pcount,
                        'link_density': round(ld, 3),
                        'preview': text[:120]
                    })
            except Exception:
                continue

        cands.sort(key=lambda x: x['text_len'], reverse=True)
        return cands[:max_count]

    def _ask_gemini_selector(self, url: str, lang: str, soup: BeautifulSoup) -> Optional[str]:
        """
        向 Gemini 2.0 Flash 詢問主文容器的 CSS 選擇器，溫度=0.3 提高穩定性。
        已修復：移除重複代碼
        """
        global GENAI_API_KEY
        if not GENAI_API_KEY:
            return None

        try:
            import google.generativeai as genai
        except Exception as e:
            print(f"[LLM] 套件不可用：{e}")
            return None

        dom_summary = self._build_dom_summary(soup)
        prompt_sys = (
            "你是資深前端工程師。根據下列 DOM 摘要，判斷最可能代表文章主文的容器，"
            "只回傳 JSON：{\"selector\":\"...\",\"confidence\":0~1,\"reason\":\"...\",\"alternatives\":[\"...\",\"...\"]}。"
            "選擇器請用 CSS（可含 class/id/層級），避免脆弱的 :nth-child。不要回原文、不要多餘說明。"
        )
        user_payload = {"url": url, "lang": lang, "candidates": dom_summary}

        try:
            genai.configure(api_key=GENAI_API_KEY)
            # 使用 Gemini 2.0 Flash，溫度=0.3 提高穩定性
            try:
                model = genai.GenerativeModel(
                    "gemini-2.0-flash-exp",
                    generation_config={"temperature": 0.3}
                )
            except Exception:
                # 後備：若 2.0 不可用，使用 1.5
                print("[LLM] Gemini 2.0 Flash 不可用，使用 1.5 Flash")
                model = genai.GenerativeModel(
                    "gemini-1.5-flash",
                    generation_config={"temperature": 0.3}
                )

            resp = model.generate_content([
                {"role": "user", "parts": [prompt_sys + "\n\n" + json.dumps(user_payload, ensure_ascii=False)[:30000]]},
            ])

            text = (getattr(resp, "text", None) or "").strip()
            if not text:
                raise ValueError("LLM 無回傳文字")

            # 移除可能的 markdown 標記
            text = text.replace("```json", "").replace("```", "").strip()

            data = json.loads(text)
            if not isinstance(data, dict):
                raise ValueError(f"LLM 回傳非物件 JSON：{text[:160]}")

            selector = data.get("selector")
            if selector and isinstance(selector, str):
                print(f"[LLM] 建議選擇器: {selector}")
                return selector
        except json.JSONDecodeError as e:
            print(f"[LLM] JSON 解析失敗：{e}, 原文：{text[:200]}")
        except Exception as e:
            print(f"[LLM] 呼叫失敗：{e}")

        return None

    # ========= 主文抽取（整合所有優化）=========
    def _extract_main_text(self, html: str, url: str = "") -> str:
        soup = BeautifulSoup(html, 'html.parser')

        # 1) 先移除常見非內容元素 + 噪音
        for tag in soup.find_all(['script', 'style', 'nav', 'footer', 'iframe', 'header', 'aside']):
            try:
                tag.decompose()
            except Exception:
                pass

        # 移除廣告、社交分享等噪音
        noisy_patterns = re.compile(
            r"(ad|ads|advert|sponsor|share|social|breadcrumb|popular|"
            r"trending|recommend|related|tags|side|sidebar|comment|widget)",
            re.I
        )
        # 先收集要刪除的元素，避免在迭代時修改
        elements_to_remove = []
        for el in soup.find_all(True):
            try:
                # 檢查元素是否仍然有效
                if el is None or not hasattr(el, 'get') or not hasattr(el, 'name'):
                    continue

                classes = el.get('class')
                if classes is None:
                    classes = []
                classes_str = ' '.join(str(c) for c in classes).lower()

                id_attr = el.get('id')
                id_str = str(id_attr).lower() if id_attr else ''

                if noisy_patterns.search(classes_str) or noisy_patterns.search(id_str):
                    # 檢查是否包含重要內容
                    try:
                        p_count = len(el.find_all('p'))
                        text_len = len(el.get_text(strip=True))
                    except Exception:
                        p_count = 0
                        text_len = 0

                    if p_count < 3 and text_len < 400:
                        elements_to_remove.append(el)
            except Exception:
                # 元素可能已被移除或無效，跳過
                continue

        # 統一移除收集到的元素
        for el in elements_to_remove:
            try:
                if el and hasattr(el, 'decompose'):
                    el.decompose()
            except Exception:
                pass

        # 2) 檢查 domain 快取
        domain = ''
        try:
            parsed = urlparse(url)
            domain = parsed.netloc
        except Exception:
            pass

        if domain and domain in DOMAIN_SELECTOR_CACHE:
            sel = DOMAIN_SELECTOR_CACHE[domain]
            node = soup.select_one(sel)
            if node:
                text = node.get_text("\n", strip=True)
                cleaned = self._clean_text(text)
                if len(cleaned) >= 200:
                    print(f"[快取命中] 使用已知選擇器: {sel}")
                    return cleaned

        # 3) 增強型 Heuristics 擇優 + 置信度
        best_node, conf, _top10 = self._best_by_heuristics(soup, url)

        # 4) 若置信度不足（<= 0.55），且有 API Key，呼叫 Gemini 要 selector
        if (best_node is None or conf <= HEURISTIC_CONF_THRESHOLD) and GENAI_API_KEY:
            print(f"[低置信度] {conf:.2f} <= {HEURISTIC_CONF_THRESHOLD}，呼叫 LLM 協助...")
            sel = self._ask_gemini_selector(url or '', 'zh-TW', soup)
            if sel:
                try:
                    node = soup.select_one(sel)
                    if node:
                        text = node.get_text("\n", strip=True)
                        cleaned = self._clean_text(text)
                        if len(cleaned) >= 200:
                            if domain:
                                DOMAIN_SELECTOR_CACHE[domain] = sel
                                print(f"[已快取] {domain} -> {sel}")
                            return cleaned
                except Exception as e:
                    print(f"[LLM選擇器失敗] {e}")

        # 5) 回退：用 heuristics 結果或整頁
        if best_node:
            text = best_node.get_text("\n", strip=True)
        else:
            text = soup.get_text("\n", strip=True)
        cleaned = self._clean_text(text)

        # 6) 若仍過短，補上 meta/og:description 導語
        if len(cleaned) < 200:
            meta_desc = None
            m = soup.find('meta', attrs={'name': 'description'})
            if m and m.get('content'):
                meta_desc = m['content']
            if not meta_desc:
                ogd = soup.find('meta', attrs={'property': 'og:description'})
                if ogd and ogd.get('content'):
                    meta_desc = ogd['content']
            if meta_desc and meta_desc not in cleaned:
                cleaned = meta_desc.strip() + "\n\n" + cleaned

        return cleaned

    def _clean_text(self, text: str) -> str:
        """清理文字：去除導覽詞、過短行、重複行、多餘空行"""
        if not text:
            return ""
        lines = []
        prev = None
        for raw in text.splitlines():
            line = re.sub(r"\s+", " ", raw).strip()
            if not line:
                continue
            if len(line) < 6:
                continue
            # 移除常見導覽詞
            if re.fullmatch(r"(首頁|登入|註冊|搜尋|分享|按讚|留言|導覽|menu|login|search|share)", line, flags=re.I):
                continue
            # 去除相鄰重複
            if prev and prev == line:
                continue
            lines.append(line)
            prev = line
        out = "\n".join(lines)
        out = re.sub(r"(\n){3,}", "\n\n", out)
        return out

    # 公開 API
    def scrape_webpage(self, url: str, hard_timeout_sec: int = 60) -> str:
        """
        爬取網頁主要內容

        Args:
            url: 目標網址
            hard_timeout_sec: 硬性超時時間（秒）

        Returns:
            str: 提取的主要內容文字

        Raises:
            UnsupportedSiteError: 不支援的網站
            TimeoutError: 超時
            Exception: 其他錯誤
        """
        if "dcard.tw" in url.lower():
            raise UnsupportedSiteError("Dcard 網站不支援爬取，已略過。")

        max_retries = 1  # 將重試交由 UI 控制，避免長時間卡住
        for attempt in range(1, max_retries + 1):
            try:
                if self.driver is None:
                    self._init_driver()
                print(f"開始爬取: {url} (第 {attempt} 次)")
                self._deadline = time.time() + hard_timeout_sec
                self._open(url)
                self._wait_for_content_load(deadline=self._deadline)
                self._clear_overlays_and_click_cta(deadline=self._deadline)
                self._scroll_page_safe(deadline=self._deadline)
                html = self.driver.page_source
                content = self._extract_main_text(html, url=url)
                if content and len(content.strip()) > 100:
                    print(f"成功，長度: {len(content)} 字元")
                    return content
                else:
                    raise ValueError("內容過短或為空")
            except UnsupportedSiteError:
                raise
            except TimeoutError as e:
                print(f"超時錯誤: {e}")
                raise
            except Exception as e:
                import traceback
                traceback.print_exc()
                msg = str(e)
                if "Binary Location" in msg:
                    print(f"[診斷] Binary Location 錯誤，CHROME_BIN={CHROME_BINARY!r} (type={type(CHROME_BINARY).__name__})")
                    print("[建議] 檢查 Chrome 是否安裝成功，或手動設定 CHROME_BIN=/usr/bin/google-chrome-stable 後重試。")
                print(f"爬取錯誤: {e}")
                try:
                    if self.driver:
                        self.driver.quit()
                except Exception:
                    pass
                self.driver = None
                if attempt == max_retries:
                    raise Exception(f"爬取失敗（單頁限時 {hard_timeout_sec}s）：{e}")
                time.sleep(0.5)

    def close(self):
        """關閉瀏覽器"""
        if self.driver:
            try:
                self.driver.quit()
            except Exception:
                pass
            self.driver = None

