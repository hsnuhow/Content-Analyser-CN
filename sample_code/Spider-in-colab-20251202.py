    def _open(self, url: str):
        """
        打開網頁，包含超時保護和錯誤處理
        """
        if self.driver is None:
            self._init_driver()

        try:
            self.driver.execute_cdp_cmd(
                "Network.setExtraHTTPHeaders",
                {"headers": {"Accept-Language": ZH_ACCEPT_LANGUAGE}},
            )
        except Exception:
            pass

        # 使用 try-except 捕獲各種超時錯誤
        max_retries = 2
        for attempt in range(1, max_retries + 1):
            try:
                print(f"[載入] 正在打開網頁 (嘗試 {attempt}/{max_retries})...")
                self.driver.get(url)
                self._apply_locale_spoofing_js()
                print("[載入] ✓ 網頁已成功載入")
                return  # 成功就退出
            except TimeoutException as e:
                print(f"[載入] ⚠️ 頁面載入超時 (嘗試 {attempt}/{max_retries}) - {url}")
                if attempt == max_retries:
                    raise TimeoutError(f"頁面載入超時，已重試 {max_retries} 次 - {url}")
                time.sleep(1)
            except Exception as e:
                error_msg = str(e).lower()
                # 識別各種超時錯誤
                if any(keyword in error_msg for keyword in ['timeout', 'timed out', 'time out', 'connection aborted', 'connection refused']):
                    print(f"[載入] ⚠️ 網路連接錯誤 (嘗試 {attempt}/{max_retries}) - {url}")
                    if attempt == max_retries:
                        raise TimeoutError(f"網路連接錯誤，已重試 {max_retries} 次 - {url}")
                    # 超時後重置 driver
                    try:
                        self.driver.execute_script("window.stop();")
                    except Exception:
                        pass
                    time.sleep(1)
                else:
                    # 非超時錯誤，直接拋出
                    raise

