def create_or_update_google_doc(doc_id: Optional[str], title: str, content: str, is_new: bool = True) -> str:
    """
    創建或更新 Google Doc

    Args:
        doc_id: 文件 ID（更新時使用）
        title: 文件標題
        content: 要插入的內容
        is_new: 是否為新文件

    Returns:
        str: 文件 ID
    """
    if not DOCS_ENABLED:
        raise RuntimeError("Google Docs API 不可用：請在 Colab 或已授權環境執行。")
    try:
        if is_new:
            doc = docs_service.documents().create(body={'title': title}).execute()
            doc_id = doc.get('documentId')
        requests = [
            {
                'insertText': {
                    'location': {'index': 1},
                    'text': f"{content}\n\n{'='*50}\n\n"
                }
            }
        ]
        docs_service.documents().batchUpdate(documentId=doc_id, body={'requests': requests}).execute()
        return doc_id
    except Exception as e:
        raise Exception(f"Google Doc 操作錯誤: {str(e)}")

