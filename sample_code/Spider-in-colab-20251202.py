def batch_update_google_doc(doc_id: str, payloads: List[str]):
    """
    使用單一 API 請求將所有內容批次插入 Google Doc。
    內容會反向插入，確保第一個網址在文件最上方 (index=1)。
    """
    if not DOCS_ENABLED:
        raise RuntimeError("Google Docs API 不可用。")
    if not doc_id:
        raise ValueError("doc_id 不可為空。")
    if not payloads:
        print("[Docs] 沒有成功爬取的內容可更新。")
        return

    requests = []
    # 反向插入，確保第一個網址在最上面 (index=1)
    for payload in reversed(payloads):
        requests.append({
            'insertText': {
                'location': {'index': 1},
                'text': f"{payload}\n\n{'='*50}\n\n"
            }
        })

    try:
        docs_service.documents().batchUpdate(
            documentId=doc_id,
            body={'requests': requests}
        ).execute()
        print(f"[Docs] ✓ 成功批次更新 {len(payloads)} 筆內容。")
    except Exception as e:
        raise Exception(f"Google Doc 批次更新錯誤: {str(e)}")


